export const ElementsErrors: React.FC = () => {
  return (
    <div className="reference-section">
      <h3>Ошибки</h3>
      
      <p>Ниже приведены типовые ошибки при работе с Selenide и взаимодействии с элементами страницы, а также пояснения, почему они возникают и как их избегать.</p>

      <div style={warningStyle}>
        <h4>1. Использование Thread.sleep</h4>
        <pre style={preStyle}><code>Thread.sleep(5000); // Ожидание 5 секунд вручную</code></pre>
        <p>Это одна из самых распространённых ошибок. Жёсткие ожидания замедляют тесты и делают их нестабильными. 
        Если элемент появится раньше — тест всё равно будет ждать. Если появится позже — тест упадёт.</p>
        <p><strong>Правильный подход — использовать встроенные ожидания:</strong></p>
        <pre style={preStyle}><code>$("#message").shouldBe(visible); // Selenide автоматически дождётся появления элемента</code></pre>
        <p>Selenide сам управляет ожиданиями, поэтому ручные паузы почти никогда не нужны.</p>
      </div>

      <div style={warningStyle}>
        <h4>2. Отсутствие проверки после действия</h4>
        <pre style={preStyle}><code>$("#submit").click(); // Клик выполнен, но результат не проверяется</code></pre>
        <p>Если после действия нет проверки, тест не подтверждает, что система изменила состояние. 
        Такой тест может «проходить», даже если функциональность сломана.</p>
        <p><strong>Правильный подход:</strong></p>
        <pre style={preStyle}><code>{`$("#submit").click();
$("#success").shouldBe(visible);`}</code></pre>
      </div>

      <div style={warningStyle}>
        <h4>3. Проверка через if вместо should</h4>
        <pre style={preStyle}><code>{`if ($("#message").isDisplayed()) {
    // логика
}`}</code></pre>
        <p>Методы вроде isDisplayed() не ожидают появления элемента. Проверка произойдёт мгновенно, и тест может упасть из-за асинхронной загрузки.</p>
        <p><strong>Правильный вариант:</strong></p>
        <pre style={preStyle}><code>$("#message").shouldBe(visible);</code></pre>
        <p>shouldBe и shouldHave содержат встроенное ожидание и делают тест стабильным.</p>
      </div>

      <div style={warningStyle}>
        <h4>4. Использование click вместо setSelected</h4>
        <pre style={preStyle}><code>$("#agree").click();</code></pre>
        <p>Если чекбокс уже выбран, click снимет его. Тест станет зависим от начального состояния.</p>
        <p><strong>Правильный вариант:</strong></p>
        <pre style={preStyle}><code>$("#agree").setSelected(true);</code></pre>
      </div>

      <div style={warningStyle}>
        <h4>5. Работа с элементом до его появления</h4>
        <pre style={preStyle}><code>{`SelenideElement button = $("#submit");
button.click();`}</code></pre>
        <p>Если страница ещё не загружена или элемент появляется динамически, может возникнуть ошибка Element not found.</p>
        <p><strong>Лучше выполнять действия сразу через цепочку или предварительно проверять видимость:</strong></p>
        <pre style={preStyle}><code>$("#submit").shouldBe(visible).click();</code></pre>
        <p>Это гарантирует, что элемент существует и доступен.</p>
      </div>

      <div style={warningStyle}>
        <h4>6. Использование индексов вместо поиска по признаку</h4>
        <pre style={preStyle}><code>$$(".item").get(2).click();</code></pre>
        <p>Если порядок элементов изменится, тест начнёт работать с другим объектом.</p>
        <p><strong>Предпочтительнее:</strong></p>
        <pre style={preStyle}><code>$$(".item").findBy(text("Оплата")).click();</code></pre>
        <p>Поиск по смысловому признаку делает тест устойчивым к изменениям порядка.</p>
      </div>

      <div style={warningStyle}>
        <h4>7. Забыли переключиться в iframe</h4>
        <pre style={preStyle}><code>{`open("/payment");
$("#card-number").setValue("1111"); // Ошибка: элемент не найден`}</code></pre>
        <p>Если элемент находится внутри iframe, поиск вне переключения приведёт к ошибке.</p>
        <p><strong>Правильный порядок:</strong></p>
        <pre style={preStyle}><code>{`switchTo().frame("payment-frame");
$("#card-number").setValue("1111");`}</code></pre>
        <p>Перед работой с элементами внутри iframe необходимо переключиться.</p>
      </div>

      <div style={warningStyle}>
        <h4>8. Забыли переключиться на новую вкладку</h4>
        <pre style={preStyle}><code>{`$("#open-details").click();
$("#order-info").shouldBe(visible); // Ошибка, если страница открылась в новой вкладке`}</code></pre>
        <p>Если открылась новая вкладка, браузер остаётся в старой.</p>
        <p><strong>Правильный порядок:</strong></p>
        <pre style={preStyle}><code>{`switchTo().window(1);
$("#order-info").shouldBe(visible);`}</code></pre>
        <p>Все действия выполняются только в активном окне.</p>
      </div>

      <div style={warningStyle}>
        <h4>9. Работа с alert без переключения</h4>
        <pre style={preStyle}><code>{`$("#delete").click();
switchTo().alert().accept();`}</code></pre>
        <p>Если alert ещё не появился, возникнет ошибка NoAlertPresentException.</p>
        <p>Лучше сначала инициировать действие, затем работать с alert. В некоторых случаях требуется небольшое ожидание через should.</p>
      </div>

      <div style={warningStyle}>
        <h4>10. Неправильная проверка текста</h4>
        <pre style={preStyle}><code>$("#status").getText().equals("Готово");</code></pre>
        <p>getText() не содержит ожидания и делает проверку мгновенной.</p>
        <p><strong>Правильный вариант:</strong></p>
        <pre style={preStyle}><code>$("#status").shouldHave(text("Готово"));</code></pre>
        <p>Метод shouldHave ожидает изменения текста в течение таймаута.</p>
      </div>
    </div>
  );
};

